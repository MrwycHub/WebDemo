namespace WebDemo.Web.WebDemo.ParentMeter
{
    public partial class Add : Page
    {
        protected void Page_Load(object sender, EventArgs e)
        {
                       
        }

        		protected void btnSave_Click(object sender, EventArgs e)
		{
			
			string strErr="";
			if(!PageValidate.IsNumber(txtMeterID.Text))
			{
				strErr+="MeterID格式错误！\\n";	
			}
			if(!PageValidate.IsNumber(txtParentID.Text))
			{
				strErr+="ParentID格式错误！\\n";	
			}

			if(strErr!="")
			{
				MessageBox.Show(this,strErr);
				return;
			}
			int MeterID=int.Parse(this.txtMeterID.Text);
			int ParentID=int.Parse(this.txtParentID.Text);

			WebDemo.Model.WebDemo.ParentMeter model=new WebDemo.Model.WebDemo.ParentMeter();
			model.MeterID=MeterID;
			model.ParentID=ParentID;

			WebDemo.BLL.WebDemo.ParentMeter bll=new WebDemo.BLL.WebDemo.ParentMeter();
			bll.Add(model);
			Maticsoft.Common.MessageBox.ShowAndRedirect(this,"保存成功！","add.aspx");

		}


        public void btnCancle_Click(object sender, EventArgs e)
        {
            Response.Redirect("list.aspx");
        }
    }
}
